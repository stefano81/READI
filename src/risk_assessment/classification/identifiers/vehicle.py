    def is_of_this_type(self, text: str) -> bool:
        text = text.upper()
        matcher = self.pattern.match(text)
        if matcher is None:
            return False

        wmi = matcher.group(1)

        if wmi not in self.world_manufacturer_identifiers:
            return False

        if wmi in self.us_wmi:
            return self._checksum_check(text)

