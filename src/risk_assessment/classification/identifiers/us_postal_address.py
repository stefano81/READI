                r"|(?:\d+\s+"
                + SUFFIX
                + r"(?:\s+\w{1,})+"
                + SEPARATOR
                + r"(?:\s+\w{3,})+"
                + SEPARATOR
                + r"(?:\s+\w{3,})+"
                + SEPARATOR
