                + r")?)?)?)"
                r"|(?:(?:(?:(?:"
                + SECONDARY_ADDRESS
                + SEPARATOR
                + r")?"
                + DELIVERY_ADDRESS_LINE
                + SEPARATOR
                + r")?"
                + CITY_NAME
                + SEPARATOR
                + r")?"
                + STATE_AND_POSSESSIONS
                + SEPARATOR
                + ZIP_CODE
                + r")"
                r"|(?:\d+(?:-\d+)?(?:\s+"
                + DIRECTIONAL
                + r")?(?:\s+[a-zA-Z]{3,}){1,4}(?:\s+\d+)?\s+"
                + SUFFIX
                + r"(?: "
                + DIRECTIONAL
                + r")?(?:\s+"
                + SECONDARY_ADDRESS
                + r")?(?:"
                + SEPARATOR
                + r"(?:\s+[a-zA-Z]{3,}){1,4})?)"
                r"|(?:\d+(?:\s+\w{3,})+(?:\s+\d+)?,?(?:\s+\w{3,})+,?(?:\s+\w{3,})+,?\s+"
                + COUNTRY
                + r"\s+\d{4,5}(?:-\d{4,5})?)"
                r"|(?:\d+\s+" + DIRECTIONAL + r"(?:\s+\w{3,})+\s+\d+,?\s+\d{3,})" + r")$",
                options,
